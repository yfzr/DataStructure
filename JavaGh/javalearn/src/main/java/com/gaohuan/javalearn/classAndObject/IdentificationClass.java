public class IdentificationClass {
    public static void main(String[] args) {
        //在传统的过程化程序设计中，必须从顶部的main函数开始编写程序
        //在面向对象程序设计中没有所谓的“顶部”
        //面向对象编程：首先从设计类开始，然后再往每个类中添加方法
        //识别类的规则：在分析问题的过程中寻找名词，而方法对应着动词【也即：类的属性名及方法名要求“见名思意”】
        System.out.println("识别类：实际生活对应的事物名词，往往对应着Java开发中的一个类，对这个事物的操作往往对应着Java类中的方法");
    }
}
